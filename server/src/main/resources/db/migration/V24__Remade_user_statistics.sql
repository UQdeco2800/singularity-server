DROP TABLE USERSTATISTICS;
CREATE TABLE USERSTATISTICS (
	STATID varchar(64) PRIMARY KEY,
	USERID varchar(64) NOT NULL,
	USERLEVEL varchar(64) NOT NULL,
	MINIONSPLAYED varchar(64) NOT NULL,
	MINIONSKILLED varchar(64) NOT NULL,
	MINIONSLOST varchar(64) NOT NULL,
	HEALTHLOST varchar(64) NOT NULL,
	HEALTHTAKEN varchar(64) NOT NULL,
	TIMESTAMP timestamp NOT NULL,
	TOTALWINS varchar(64) NOT NULL,
	TOTALLOSSES varchar(64) NOT NULL,
	TOTALHOURS varchar(64) NOT NULL,
	TOTALMINUTES varchar(64) NOT NULL,
	FOWLWINS varchar(64) NOT NULL,
	FOWLLOSSES varchar(64) NOT NULL,
	FOWLHOURS varchar(64) NOT NULL,
	FOWLMINUTES varchar(64) NOT NULL,
	QUACKWINS varchar(64) NOT NULL,
	QUACKLOSSES varchar(64) NOT NULL,
	QUACKHOURS varchar(64) NOT NULL,
	QUACKMINUTES varchar(64) NOT NULL,
	RALLARDWINS varchar(64) NOT NULL,
	RALLARDLOSSES varchar(64) NOT NULL,
	RALLARDHOURS varchar(64) NOT NULL,
	RALLARDMINUTES varchar(64) NOT NULL,
	FOREIGN KEY (USERID) REFERENCES USERS(USERID)
);